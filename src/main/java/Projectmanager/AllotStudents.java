    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
            int selectedRow = jTable1.getSelectedRow();
    if (selectedRow >= 0) {
        DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
        String FieldAssesment = jTextFieldAssesment.getText();
        String FieldSupervisor = jTextFieldSupervisor.getText();
        String Field2marker = jTextField2marker.getText();
        String FieldProType = jTextFieldProType.getText();


        tblModel.setValueAt(FieldAssesment, selectedRow, 0); // Update drinks
        tblModel.setValueAt(FieldSupervisor, selectedRow, 1);  // Update price
        tblModel.setValueAt(Field2marker, selectedRow, 2);  // Update quantity
        tblModel.setValueAt(FieldProType, selectedRow, 3);  // Update quantity

        // Update the data in the drinksData.txt file
        updateDataInFile(FieldAssesment, FieldSupervisor, Field2marker, FieldProType);

        JOptionPane.showMessageDialog(this, "Data updated successfully!");
        jTextFieldAssesment.setText("");
        jTextFieldSupervisor.setText("");
        jTextField2marker.setText("");
        jTextFieldProType.setText("");

    } else {
        JOptionPane.showMessageDialog(this, "Please select a row to update.");
    }
        
    }//GEN-LAST:event_jButton3ActionPerformed

