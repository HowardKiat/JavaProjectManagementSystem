        private void table() {
        DefaultTableModel tableModel = new DefaultTableModel();

        tableModel.addColumn("Assessment Type");
        tableModel.addColumn("Supervisor");
        tableModel.addColumn("Second Marker");
        tableModel.addColumn("Project Type");

        try {
            FileReader fileReader = new FileReader("Assessment_details.txt");
            BufferedReader bufferedReader = new BufferedReader(fileReader);

            String line;
            while ((line = bufferedReader.readLine()) != null) {
                String[] data = line.split(", ");
                tableModel.addRow(data);
            }

            bufferedReader.close();
        } catch (IOException e) {
            e.printStackTrace();
        }

        jTable1.setModel(tableModel);       
    }
        
        private void saveDataToFile(String[] data) {
    try {
        StringBuilder entry = new StringBuilder();
        for (int i = 0; i < data.length; i++) {
            entry.append(data[i]);
            if (i < data.length - 1) {
                entry.append(", ");
            }
        }

        BufferedWriter writer = new BufferedWriter(new FileWriter("Assessment_details.txt", true)); // Append mode
        writer.write(entry.toString());
        writer.newLine(); // Add a newline character to separate entries
        writer.close();
    } catch (IOException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error saving data to file.", "Error", JOptionPane.ERROR_MESSAGE);
    }
}
private void saveTableDataToFile() {
    try {
        BufferedWriter writer = new BufferedWriter(new FileWriter("Assessment_details.txt"));

        for (int row = 0; row < jTable1.getRowCount(); row++) {
            StringBuilder entry = new StringBuilder();
            for (int col = 0; col < jTable1.getColumnCount(); col++) {
                entry.append(jTable1.getValueAt(row, col));
                if (col < jTable1.getColumnCount() - 1) {
                    entry.append(", ");
                }
            }
            writer.write(entry.toString());
            writer.newLine();
        }

        writer.close();
    } catch (IOException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error saving data.", "Error", JOptionPane.ERROR_MESSAGE);
    }
}

    private void updateDataInFile(String projectName, String newType, String newSupervisor, String newSecondMarker) {
        try {
            Path filePath = Paths.get("Assessment_details.txt");
            List<String> lines = Files.readAllLines(filePath, StandardCharsets.UTF_8);

            for (int i = 0; i < lines.size(); i++) {
                String[] parts = lines.get(i).split(", ");
                if (parts.length >= 4 && parts[0].equals(projectName)) {
                    lines.set(i, String.format("%s, %s, %s, %s", projectName, newType, newSupervisor, newSecondMarker));
                    break;
                }
            }

        Files.write(filePath, lines, StandardCharsets.UTF_8);
    } catch (IOException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error updating data in file.", "Error", JOptionPane.ERROR_MESSAGE);
    }
}

